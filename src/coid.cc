const char* crefl_asn1_oid_desc(const char *oid)
{
	static std::map<std::string,std::string> map;

	/* this initializer is not thread-safe */

	if (map.size() == 0) {
		auto o = oid_map;
		while (o->oid && o->name) {
			map[o->oid] = o->name;
			o++;
		}
	}

	auto it = map.find(oid);
	if (it != map.end()) return it->second.c_str();
	else return "";
}
